    }
  },
  computed: {
    user() {
      return this.$store.state.auth.user
    },
    isAuthenticated() {
      return this.$store.getters['auth/isAuthenticated']
    }
  },
  methods: {
    openNav() {
      this.open = true
      this.animate()
    },
    closeNav() {
      this.open = false
      this.animate()
    },
    nextPage() {
      setTimeout(() => { 
        this.open = false
        this.animate()
      }, 300)
    },
